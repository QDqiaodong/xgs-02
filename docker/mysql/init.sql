CREATE DATABASE IF NOT EXISTS recipe_db DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

USE recipe_db;

CREATE TABLE IF NOT EXISTS recipe (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    title VARCHAR(255) NOT NULL COMMENT '菜谱标题',
    description TEXT COMMENT '菜谱描述',
    cover_image VARCHAR(500) COMMENT '封面图片',
    author VARCHAR(100) COMMENT '作者',
    cook_time INT COMMENT '烹饪时间(分钟)',
    difficulty TINYINT COMMENT '难度:1-简单,2-中等,3-困难',
    favorite_count INT DEFAULT 0 COMMENT '收藏数',
    view_count INT DEFAULT 0 COMMENT '浏览数',
    tags VARCHAR(500) COMMENT '标签,逗号分隔',
    ingredients TEXT COMMENT '食材清单(JSON)',
    steps TEXT COMMENT '烹饪步骤(JSON)',
    tips TEXT COMMENT '小贴士',
    status TINYINT DEFAULT 1 COMMENT '状态:0-下架,1-上架',
    is_draft TINYINT DEFAULT 0 COMMENT '是否草稿:0-否,1-是',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted TINYINT DEFAULT 0 COMMENT '逻辑删除:0-未删,1-已删',
    INDEX idx_status (status, is_draft, deleted),
    INDEX idx_favorite (favorite_count),
    INDEX idx_created (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='菜谱表';

CREATE TABLE IF NOT EXISTS favorite (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    user_id BIGINT NOT NULL COMMENT '用户ID',
    recipe_id BIGINT NOT NULL COMMENT '菜谱ID',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    deleted TINYINT DEFAULT 0 COMMENT '逻辑删除:0-未删,1-已删',
    UNIQUE KEY uk_user_recipe (user_id, recipe_id, deleted),
    INDEX idx_recipe (recipe_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='收藏表';

CREATE TABLE IF NOT EXISTS comment (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    user_id BIGINT NOT NULL COMMENT '用户ID',
    username VARCHAR(100) COMMENT '用户名',
    recipe_id BIGINT NOT NULL COMMENT '菜谱ID',
    content TEXT NOT NULL COMMENT '评论内容',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted TINYINT DEFAULT 0 COMMENT '逻辑删除:0-未删,1-已删',
    INDEX idx_recipe_created (recipe_id, created_at DESC),
    INDEX idx_user (user_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='评论表';

CREATE TABLE IF NOT EXISTS view_history (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    user_id BIGINT NOT NULL COMMENT '用户ID',
    recipe_id BIGINT NOT NULL COMMENT '菜谱ID',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '浏览时间',
    deleted TINYINT DEFAULT 0 COMMENT '逻辑删除:0-未删,1-已删',
    UNIQUE KEY uk_user_recipe (user_id, recipe_id, deleted),
    INDEX idx_user_created (user_id, created_at DESC),
    INDEX idx_recipe (recipe_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='浏览历史表';

