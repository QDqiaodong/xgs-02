CREATE TABLE IF NOT EXISTS ingredient_nutrition (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    name VARCHAR(100) NOT NULL COMMENT '食材名称',
    alias VARCHAR(255) COMMENT '别名,逗号分隔',
    calories DECIMAL(10,2) DEFAULT 0 COMMENT '热量(千卡/100g)',
    protein DECIMAL(10,2) DEFAULT 0 COMMENT '蛋白质(克/100g)',
    fat DECIMAL(10,2) DEFAULT 0 COMMENT '脂肪(克/100g)',
    carbohydrate DECIMAL(10,2) DEFAULT 0 COMMENT '碳水化合物(克/100g)',
    fiber DECIMAL(10,2) DEFAULT 0 COMMENT '膳食纤维(克/100g)',
    sodium DECIMAL(10,2) DEFAULT 0 COMMENT '钠(毫克/100g)',
    category VARCHAR(50) COMMENT '食材分类',
    unit VARCHAR(20) DEFAULT 'g' COMMENT '计量单位',
    remark VARCHAR(500) COMMENT '备注',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted TINYINT DEFAULT 0 COMMENT '逻辑删除:0-未删,1-已删',
    UNIQUE KEY uk_name (name, deleted),
    INDEX idx_category (category),
    INDEX idx_name (name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='食材营养信息表';

